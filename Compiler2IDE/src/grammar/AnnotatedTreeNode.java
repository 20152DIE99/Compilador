package grammar;

public class NodeAnnotedTree {
//	Fields
	private String type;
	private Object valor;
	
	public NodeAnnotedTree() {
		super();
	}
	
//	Getters and Setters
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Object getValor() {
		return valor;
	}
	public void setValor(Object valor) {
		this.valor = valor;
	}	
}
